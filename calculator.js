const calculator = {
    addNumber(num1, num2) {
        return num1 + num2;
    },

    subtractNumber(num1, num2) {
        return num1 - num2;
    },

    multiplyNumber(num1, num2) {
        return num1 * num2;
    },

    divideNumber(num1, num2) {
        return num1 / num2;
    }
    
    
}

module.exports = calculator;